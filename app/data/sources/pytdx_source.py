    # -------------------- 板块指数日线获取 --------------------

    @staticmethod
    def get_tdx_index_daily(tdx_code: str, market: int = 1,
                            start_date: str = '20200101',
                            end_date: Optional[str] = None,
                            max_bars: int = 2000) -> Optional['pd.DataFrame']:
        """
        从通达信服务器获取板块指数日线数据（使用连接池）
        tdx_code: 通达信代码（如 880301、8811xx）
        market: 1=沪市, 0=深市（板块指数用 1）
        start_date: 起始日期 YYYYMMDD
        end_date: 截止日期 YYYYMMDD（可选，None 表示不做上限过滤）
        max_bars: 最多获取的K线数量（默认2000，增量更新可设为200）
        """
        if not HAS_PYTDX or not HAS_PANDAS:
            return None
        api = _get_connection()
        if not api:
            return None
        try:
            all_bars = []
            # 每次最多 800 条，循环获取，限制最大数量
            for start in range(0, max_bars, 800):
                bars = api.get_index_bars(9, market, tdx_code, start, 800)
                if not bars:
                    break
                all_bars.extend(bars)
                if len(bars) < 800:
                    break

            if not all_bars:
                return None

            df = api.to_df(all_bars)
            if df.empty:
                return None

            # 转换列名
            if 'datetime' in df.columns:
                df['trade_date'] = pd.to_datetime(df['datetime']).dt.strftime('%Y%m%d')
            else:
                df['trade_date'] = pd.to_datetime(
                    df.apply(lambda r: f"{int(r.get('year', 2020))}-{int(r.get('month', 1)):02d}-{int(r.get('day', 1)):02d}",
                             axis=1)).dt.strftime('%Y%m%d')

            # 统一字段名
            rename_map = {
                'open': 'open',
                'close': 'close',
                'high': 'high',
                'low': 'low',
                'vol': 'vol',
                'amount': 'amount',
            }
            df = df.rename(columns=rename_map)

            # 筛选日期范围
            df = df[df['trade_date'] >= start_date].copy()
            if end_date:
                df = df[df['trade_date'] <= str(end_date)].copy()
            df = df[['trade_date', 'open', 'close', 'high', 'low', 'vol', 'amount']].copy()
            df['code'] = tdx_code
            return df
        except Exception:
            return None
        finally:
            _return_connection(api)

    # -------------------- 个股日线获取 --------------------

    @staticmethod
    def get_stock_daily(stock_code: str,
                        start_date: str = '20200101',
                        end_date: Optional[str] = None) -> Optional['pd.DataFrame']:
        """
        获取个股日线数据（使用连接池）
        """
        if not HAS_PYTDX or not HAS_PANDAS:
            return None
        # 市场推断：6 开头为沪市，其他为深市
        market = MARKET_SH if stock_code.startswith(('6', '8', '9')) else MARKET_SZ
        api = _get_connection()
        if not api:
            return None
        try:
            all_bars = []
            for start in range(0, 10000, 800):
                bars = api.get_security_bars(9, market, stock_code, start, 800)
                if not bars:
                    break
                all_bars.extend(bars)
                if len(bars) < 800:
                    break
            if not all_bars:
                return None

            df = api.to_df(all_bars)
            if df.empty:
                return None

            if 'datetime' in df.columns:
                df['trade_date'] = pd.to_datetime(df['datetime']).dt.strftime('%Y%m%d')
            else:
                df['trade_date'] = pd.to_datetime(
                    df.apply(lambda r: f"{int(r.get('year', 2020))}-{int(r.get('month', 1)):02d}-{int(r.get('day', 1)):02d}",
                             axis=1)).dt.strftime('%Y%m%d')

            rename_map = {
                'open': 'open',
                'close': 'close',
                'high': 'high',
                'low': 'low',
                'vol': 'vol',
                'amount': 'amount',
            }
            df = df.rename(columns=rename_map)
            df = df[df['trade_date'] >= start_date].copy()
            if end_date:
                df = df[df['trade_date'] <= str(end_date)].copy()
            df = df[['trade_date', 'open', 'close', 'high', 'low', 'vol', 'amount']].copy()
            df['code'] = stock_code
            return df
        except Exception:
            return None
        finally:
            _return_connection(api)

