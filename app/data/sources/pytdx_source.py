    @staticmethod
    def get_ths_block_index(block_name: str, board_type: str = '概念',
                            start_date: str = '20200101') -> Optional['pd.DataFrame']:
        """
        获取板块指数日线（优先使用通达信 880XXX/881XXX 直连获取）
        对于无直接指数代码的板块，返回 None（由调用方聚合成分股）
        使用智能匹配：板块名 -> 通达信 880XXX/881XXX 指数名称
        """
        if not HAS_PYTDX or not HAS_PANDAS:
            return None

        # 扫描通达信 880/881 指数名称（一次扫描，缓存结果）
        if not hasattr(PytdxSource, '_tdx_name_cache'):
            PytdxSource._tdx_name_cache = PytdxSource._scan_tdx_index_names()
        tdx_name_to_code = PytdxSource._tdx_name_cache
        if not tdx_name_to_code:
            return None

        # 智能匹配板块名 -> 通达信代码
        tdx_code = PytdxSource._match_block_to_tdx(block_name, tdx_name_to_code)
        if not tdx_code:
            return None

        return PytdxSource.get_tdx_index_daily(tdx_code, 1, start_date)


