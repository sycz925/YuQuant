# 连接池配置
CONNECTION_POOL_SIZE = 8  # 连接池大小
_connection_pool = Queue(maxsize=CONNECTION_POOL_SIZE)
_pool_lock = threading.Lock()
_pool_initialized = False


def _init_connection_pool():
    """初始化连接池"""
    global _pool_initialized
    if _pool_initialized:
        return
    with _pool_lock:
        if _pool_initialized:
            return
        for _ in range(CONNECTION_POOL_SIZE):
            api = TdxHq_API()
            connected = False
            for host, port in TDX_SERVERS:
                try:
                    if api.connect(host, port, time_out=5):
                        connected = True
                        break
                except Exception:
                    continue
            if connected:
                try:
                    _connection_pool.put_nowait(api)
                except Full:
                    try:
                        api.disconnect()
                    except Exception:
                        pass
        _pool_initialized = True


def _get_connection():
    """从连接池获取连接"""
    _init_connection_pool()
    try:
        return _connection_pool.get_nowait()
    except Empty:
        # 池为空，创建新连接
        api = TdxHq_API()
        for host, port in TDX_SERVERS:
            try:
                if api.connect(host, port, time_out=5):
                    return api
            except Exception:
                continue
        return None


def _return_connection(api):
    """归还连接到连接池"""
    if api is None:
        return
    try:
        # 检查连接是否仍然有效
        api.get_security_count(0)  # 简单测试连接
        _connection_pool.put_nowait(api)
    except Exception:
        # 连接已失效，关闭并丢弃
        try:
            api.disconnect()
        except Exception:
            pass


