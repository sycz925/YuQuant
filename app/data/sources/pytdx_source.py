    @staticmethod
    def get_stock_basics() -> Optional['pd.DataFrame']:
        """获取股票基础信息（新版 pytdx：使用 get_security_list 遍历两市，使用连接池）"""
        if not HAS_PYTDX or not HAS_PANDAS:
            return None
        api = _get_connection()
        if not api:
            return None
        try:
            all_stocks = []
            for market in [MARKET_SH, MARKET_SZ]:
                count = api.get_security_count(market)
                if not count or count <= 0:
                    continue
                for start in range(0, min(count, 50000), 1000):
                    items = api.get_security_list(market, start)
                    if not items:
                        continue
                    for s in items:
                        code = s.get('code', '')
                        name = s.get('name', '')
                        if code and name and len(code) == 6 and code.isdigit():
                            all_stocks.append({
                                'stock_code': code,
                                'stock_name': name,
                                'market': market,
                                'list_date': '19900101',
                                'is_st': 'ST' in name,
                                'suspend': False
                            })
            if not all_stocks:
                return None
            return pd.DataFrame(all_stocks)
        except Exception:
            return None
        finally:
            _return_connection(api)

    @staticmethod
    def get_daily_data(stock_code: str, start_date: str, end_date: str):
        """获取个股日线 (兼容接口, 返回 (df, source))"""
        df = PytdxSource.get_stock_daily(stock_code, start_date)
        if df is None or (hasattr(df, 'empty') and df.empty):
            return None, ''
        if end_date:
            df = df[df['trade_date'] <= end_date].copy()
        return df, 'pytdx'

    @staticmethod
    def _blocks_to_df(blocks: List[Dict], source_label: str) -> Optional['pd.DataFrame']:
        """将板块 List[Dict] 转为 pandas DataFrame（兼容旧接口）"""
        if not blocks or not HAS_PANDAS:
            return None
        rows = []
        for b in blocks:
            rows.append({
                'block_name': b.get('name', ''),
                'block_code': '',
                'source': source_label,
                'stock_codes': b.get('stock_codes', []),
                'stock_count': len(b.get('stock_codes', [])),
            })
        return pd.DataFrame(rows)

