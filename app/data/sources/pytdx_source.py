    @staticmethod
    def _scan_tdx_index_names() -> Optional[Dict[str, str]]:
        """
        扫描通达信服务器中所有 880XXX/881XXX 指数名称（使用连接池）
        返回: {板块名称: tdx_code}
        """
        if not HAS_PYTDX:
            return None
        api = _get_connection()
        if not api:
            return None
        try:
            result = {}
            count = api.get_security_count(1)
            for start in range(0, min(count or 0, 30000), 1000):
                items = api.get_security_list(1, start)
                if not items:
                    continue
                for s in items:
                    code = s.get('code', '')
                    name = s.get('name', '')
                    if code and name and (code.startswith('880') or
                                            code.startswith('881')):
                        result[name] = code
                        # 也把 code 作为 key（便于直接通过 code 查找）
                        result[code] = code
            return result
        except Exception:
            return None
        finally:
            _return_connection(api)

    @staticmethod
    def _match_block_to_tdx(block_name: str, tdx_name_to_code: Dict[str, str]) -> str:
        """
        将板块名称匹配到通达信 880XXX/881XXX 指数代码
        返回: tdx_code 或 ''（无匹配）
        """
        if not tdx_name_to_code or not block_name:
            return ''

        # 1) 精确匹配
        if block_name in tdx_name_to_code:
            return tdx_name_to_code[block_name]

        # 2) 别名映射匹配
        if block_name in PytdxSource.BLOCK_ALIAS_MAP:
            alias = PytdxSource.BLOCK_ALIAS_MAP[block_name]
            if alias in tdx_name_to_code:
                return tdx_name_to_code[alias]

        # 3) 去除括号内容后匹配（如"股权转让(并购重组)" -> "股权转让"）
        import re
        clean_name = re.sub(r'[（(].*?[)）]', '', block_name).strip()
        if clean_name and clean_name != block_name:
            if clean_name in tdx_name_to_code:
                return tdx_name_to_code[clean_name]
            if clean_name in PytdxSource.BLOCK_ALIAS_MAP:
                alias = PytdxSource.BLOCK_ALIAS_MAP[clean_name]
                if alias in tdx_name_to_code:
                    return tdx_name_to_code[alias]

        # 4) 去除常见后缀（"概念"、"板块"）后匹配
        for suffix in ['概念', '板块', '指数', '50', '100', '300', 'A']:
            stripped = clean_name.replace(suffix, '').strip()
            if stripped and stripped in tdx_name_to_code:
                return tdx_name_to_code[stripped]
            if stripped and stripped in PytdxSource.BLOCK_ALIAS_MAP:
                alias = PytdxSource.BLOCK_ALIAS_MAP[stripped]
                if alias in tdx_name_to_code:
                    return tdx_name_to_code[alias]

        # 5) 包含关系匹配（t_name 包含 block_name 或 block_name 包含 t_name）
        for t_name, t_code in tdx_name_to_code.items():
            if len(t_name) >= 2 and len(clean_name) >= 2:
                if t_name in clean_name or clean_name in t_name:
                    return t_code

        # 6) 别名映射的包含关系匹配（更宽松）
        for key, alias in PytdxSource.BLOCK_ALIAS_MAP.items():
            if (key in clean_name or clean_name in key) and alias in tdx_name_to_code:
                return tdx_name_to_code[alias]

        return ''

