    # -------------------- 板块文件解析 --------------------

    @staticmethod
    def _download_block_file(api: 'TdxHq_API', filename: str) -> Optional[bytes]:
        """下载板块文件"""
        try:
            meta = api.get_block_info_meta(filename)
            if not meta or meta['size'] <= 0:
                return None
            total = meta['size']
            chunk = 0x7530
            content = bytearray()
            for start in range(0, total, chunk):
                data = api.get_block_info(filename, start, min(chunk, total - start))
                if data:
                    content.extend(data)
            return bytes(content)
        except Exception:
            return None

    @staticmethod
    def _parse_block_file(data: bytes) -> List[Dict]:
        """
        解析通达信新版板块文件（block_gn.dat / block_zs.dat / block_fg.dat）
        返回: [{'name': '板块名称', 'stock_codes': ['000001', ...]}, ...]
        """
        if not data or len(data) < 0x200:
            return []

        blocks = []
        pos = 0x0140  # 文件头后开始

        while pos < len(data):
            # 跳过空字节
            while pos < len(data) and data[pos] == 0:
                pos += 1
            if pos >= len(data):
                break

            # 寻找板块名称开始位置
            found_name_start = -1
            for try_pos in range(pos, min(pos + 80, len(data))):
                if try_pos + 1 < len(data):
                    b1 = data[try_pos]
                    b2 = data[try_pos + 1]
                    # GBK 中文字符: 0x8140-0xFEFE
                    if 0x81 <= b1 <= 0xFE and 0x40 <= b2 <= 0xFE:
                        found_name_start = try_pos
                        break
                    # 数字开头的板块名称（如 5G、3D）
                    elif 0x30 <= b1 <= 0x39 and 0x81 <= b2 <= 0xFE:
                        try:
                            prefix = data[try_pos:try_pos + 2].decode('gbk', errors='ignore')
                            if prefix.startswith(('5G', '3D', '2B')):
                                found_name_start = try_pos
                                break
                        except Exception:
                            pass

            if found_name_start < 0:
                pos += 4
                continue

            # 从 found_name_start 读取板块名称（以 0x00 结尾）
            name_end = data.find(b'\x00', found_name_start)
            if name_end < 0 or name_end - found_name_start > 32 or name_end - found_name_start < 2:
                pos = found_name_start + 1
                continue

            try:
                block_name = data[found_name_start:name_end].decode('gbk').strip()
            except Exception:
                pos = found_name_start + 1
                continue

            if len(block_name) < 2 or len(block_name) > 16:
                pos = name_end + 1
                continue

            # 过滤：板块名称应主要包含中文字符或数字字母组合
            valid_chars = sum(1 for c in block_name if
                              '\u4e00' <= c <= '\u9fff' or c.isdigit() or c.isalpha() or c in '+-*')
            if valid_chars < 2:
                pos = name_end + 1
                continue

            # 跳过标志位：名称后 00 + 2-4 字节 + 02 00 00 00
            after_name = name_end + 1
            flag_pos = data.find(b'\x02\x00', after_name, after_name + 16)
            if flag_pos < 0:
                pos = after_name
                continue

            # 解析股票代码
            # 如果是 02 00 00 00 格式，从 +4 开始；否则从 +2 开始
            if flag_pos + 4 <= len(data) and data[flag_pos + 2:flag_pos + 4] == b'\x00\x00':
                codes_start = flag_pos + 4
            else:
                codes_start = flag_pos + 2

            stock_codes = []
            code_pos = codes_start
            moved_to_next = False
            while code_pos < len(data):
                if data[code_pos] == 0:
                    # 检查是否为分隔符或大量空字节（板块分界）
                    zeros_count = 0
                    while code_pos + zeros_count < len(data) and data[code_pos + zeros_count] == 0:
                        zeros_count += 1
                    if zeros_count > 16:
                        next_pos = code_pos + zeros_count
                        pos = next_pos
                        moved_to_next = True
                        break
                    code_pos += 1
                    continue

                # 尝试读取 6 位 ASCII 股票代码
                code_bytes = data[code_pos:code_pos + 6]
                try:
                    code_str = code_bytes.decode('ascii')
                except Exception:
                    # 可能是 GBK 中文（新板块名称开始）
                    if code_pos + 1 < len(data):
                        b1 = data[code_pos]
                        b2 = data[code_pos + 1]
                        if 0x81 <= b1 <= 0xFE and 0x40 <= b2 <= 0xFE:
                            pos = code_pos
                            moved_to_next = True
                            break
                    pos = code_pos + 1
                    moved_to_next = True
                    break

                if code_str.isdigit() and len(code_str) == 6 and \
                        code_pos + 6 < len(data) and data[code_pos + 6:code_pos + 7] == b'\x00':
                    stock_codes.append(code_str)
                    code_pos += 7
                    continue

                # 不是股票代码
                if code_pos + 1 < len(data):
                    b1 = data[code_pos]
                    b2 = data[code_pos + 1]
                    if 0x81 <= b1 <= 0xFE and 0x40 <= b2 <= 0xFE:
                        pos = code_pos
                        moved_to_next = True
                        break

                pos = code_pos + 1
                moved_to_next = True
                break

            if not moved_to_next:
                pos = code_pos + 1

            if stock_codes:
                blocks.append({'name': block_name, 'stock_codes': stock_codes})

        return blocks

